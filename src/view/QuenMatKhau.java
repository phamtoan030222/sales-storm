    private void btn_otpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_otpActionPerformed
        // TODO add your handling code here:
       String email = txt_email.getText().trim();
       if(ValidationUtil.isEmpty(email)) {
           lbl_email.setText("Vui lòng nhập email!");
           return;
       }
       
       if (nhanVienService.timNhanVienTheoEmail(email)) {
           String otp = EmailUtil.sendOTP(email);
           if(otp != null) {
           JOptionPane.showMessageDialog(this, "Mã OTP đã được gửi!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
           new VerifyOTPDialog(this, otp, email).setVisible(true);
           dispose();
           return;
           } else {
           JOptionPane.showMessageDialog(this, "Gửi OTP thất bại!", "Lỗi", JOptionPane.ERROR_MESSAGE);
           }
       } else {
           lbl_email.setText("Email không tồn tại!");
       }     
    }//GEN-LAST:event_btn_otpActionPerformed

